class Solution {
    public void flatten(TreeNode root) {
        if (root == null) return;

        Deque<TreeNode> stack = new LinkedList<>();
        TreeNode curr = root;
        TreeNode prev = curr;
        // å°†å…¨éƒ¨èŠ‚ç‚¹è½¬åŒ–ä¸ºå·¦ç»“ç‚¹
        while (curr != null || !stack.isEmpty()) {
            while (curr != null) {
                prev = curr;
                stack.push(curr);
                curr = curr.left;
            }
            if (!stack.isEmpty()) {
                TreeNode node = stack.pop();
                prev.left = node.right;
                curr = node.right;
            }
        }

        // è½¬åŒ–ä¸ºå³èŠ‚ç‚¹
        curr = root;
        while (curr != null) {
            curr.right = curr.left;
            curr.left = null;
            curr = curr.right;
        }
    }
}
