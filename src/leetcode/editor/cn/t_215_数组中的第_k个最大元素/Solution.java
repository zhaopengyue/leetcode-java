class Solution {
    public int findKthLargest(int[] nums, int k) {
        int l = 0, r = nums.length - 1;
        while (l <= r) {
            int index = oneSort(l, r, nums);
            if (index == k - 1) {
                return nums[index];
            } else if (index < k - 1) {
                l = index + 1;
            } else {
                r = index - 1;
            }
        }
        return 0;
    }

    private int oneSort(int l, int r, int[] nums) {
        int flag = nums[r], i = l, j = l;
        while (j <= r - 1) {
            if (nums[j] > flag) {
                swap(nums, i, j);
                i++;
            }
            j++;
        }
        swap(nums, i, r);
        return i;
    }

    private void swap(int[] nums, int i, int j) {
        int tmp = nums[i];
        nums[i] = nums[j];
        nums[j] = tmp;
    }
}
