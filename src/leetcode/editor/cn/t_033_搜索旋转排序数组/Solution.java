class Solution {
    public int search(int[] nums, int target) {
        int l = 0;
        int r = nums.length - 1;

        while (l <= r) {
            int m = (l + r) >> 1;
            int mv = nums[m];

            if (mv == target) return m;

            if (mv < nums[0]) {
                // 0~mä¸ºæ— åºåŒº, m~rä¸ºæœ‰åºåŒº
                if (target > mv && target < nums[0]) {
                    l = m + 1;
                } else {
                    // å…¶ä½™æƒ…å†µçš†ä½äºå·¦ä¾§
                    r = m - 1;
                }
            } else {
                // 0 ~ mä¸ºæœ‰åºåŒº, m~rä¸ºæ— åºåŒº
                if (target < mv && target >= nums[0]) {
                    r = m - 1;
                } else {
                    l = m + 1;
                }
            }

        }

        return -1;
    }
}
