class Solution {
    public int runeReserve(int[] runes) {
        if (runes.length <= 1) return runes.length;

        Arrays.sort(runes);
        int maxNum = 1;

        // è®°å½•å½“å‰é˜¶æ®µå¼€å§‹pos
        int i = 0;
        int j = 1;
        while (j < runes.length) {
            if (runes[j] - runes[j - 1] > 1) {
                // é‡ç½®i, å¹¶è®¡ç®—å½“å‰é•¿åº¦
                maxNum = Math.max(j - i, maxNum);
                i = j;
            }
            j++;
        }

        maxNum = Math.max(j - i, maxNum);

        return maxNum;
    }
}
