class Solution {
    public int[] productExceptSelf(int[] nums) {
        int len = nums.length;
        int[] res = new int[len];

        int left = 1, right = 1;
        res[0] = left;
        res[len-1] = right;

        for (int i = 1; i < len; i++) {
            left *= nums[i - 1];
            res[i] = left;
        }

        for (int i = len - 2; i >= 0; i--) {
            right *= nums[i + 1];
            res[i] *= right;
        }

        return res;
    }
}
